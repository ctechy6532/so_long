#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*str;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	str = malloc(count * size);
	if (!str)
		return (0);
	ft_memset(str, 0, count * size);
	return (str);
}
